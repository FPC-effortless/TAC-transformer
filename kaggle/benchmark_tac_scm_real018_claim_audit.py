CLAIMS = (
    "benchmark_requires_family_and_parameter",
    "latent_proxy_recovers_executable_structure",
    "explicit_bound_slots_are_sufficient",
    "explicit_bound_slots_generalize_compositionally",
    "non_oracle_verifier_repairs_corrupted_slots",
)

ALLOWED_VERDICTS = {
    "REAL CLAIMS NOT VALIDATED",
    "PARTIAL: EXPLICIT SLOT SYMBOLIC SUBSTRATE ONLY",
    "VALIDATED: LATENT EXECUTABLE STRUCTURE AND NON-ORACLE REPAIR",
}


def run_tac_scm_real018_claim_audit(
    *,
    seeds: Iterable[int] = range(10),
    train_samples: int = 256,
    eval_samples: int = 256,
    steps: int = 10,
) -> dict[str, Any]:
    del steps
    seeds = list(seeds)
    per_seed: list[dict[str, Any]] = []
    for seed in seeds:
        train = generate_balanced_executable_dataset("train", train_samples, seed)
        test = generate_balanced_executable_dataset("test", eval_samples, seed)
        split = build_heldout_split(seed, train_samples, eval_samples)
        row = {
            "seed": seed,
            **prefix("benchmark", evaluate_benchmark_validity(seed, train_samples, eval_samples)),
            **prefix("latent", evaluate_latent_proxy_recovery(train, test, seed)),
            **prefix("slots", evaluate_explicit_slot_sufficiency(test)),
            **prefix("composition", evaluate_composition(split)),
            **prefix("repair", evaluate_non_oracle_repair(split["heldout_eval"], seed)),
        }
        per_seed.append(row)

    aggregate = aggregate_rows(per_seed)
    claims = evaluate_claims(aggregate)
    verdict = compute_verdict(claims)
    return {
        "benchmark": "TAC-SCM-REAL018 corrected research claim audit",
        "status": "completed",
        "claims": list(CLAIMS),
        "seeds": seeds,
        "train_samples": train_samples,
        "eval_samples": eval_samples,
        "aggregate_metrics": aggregate,
        "per_seed_metrics": per_seed,
        "claim_results": claims,
        "inference_boundary": {
            "latent_proxy": "receives only proxy features generated by extract_features('real002')",
            "explicit_slot_symbolic": "receives explicit family_id, parameter_id, and binding_id",
            "non_oracle_repair": "receives only corrupted slot fields; no gold slot and no corruption label",
            "oracle_repair": "reported only as an upper bound, never selected as evidence",
        },
        "verdict": verdict,
        "interpretation": interpret(claims, verdict),
    }


def evaluate_benchmark_validity(seed: int, train_samples: int, eval_samples: int) -> dict[str, float]:
    result = run_tac_scm_real011(seeds=[seed], train_samples=train_samples, eval_samples=eval_samples)
    oracle = result["variant_results"]["family_parameter_oracle"]
    return {
        "oracle_accuracy": oracle["family_parameter_oracle_accuracy"],
        "family_only_accuracy": oracle["family_only_accuracy"],
        "parameter_only_accuracy": oracle["parameter_only_accuracy"],
        "query_only_accuracy": oracle["query_only_accuracy"],
        "memorization_accuracy": oracle["memorization_accuracy"],
        "family_shuffle_drop": oracle["family_shuffle_drop"],
        "parameter_shuffle_drop": oracle["parameter_shuffle_drop"],
        "valid": float(result["verdict"] == "VALID EXECUTABLE STRUCTURE BENCHMARK"),
    }


def evaluate_latent_proxy_recovery(train: list[BalancedExample], test: list[BalancedExample], seed: int) -> dict[str, float]:
    train_features = [extract_features("real002", example, seed, split="train") for example in train]
    test_features = [extract_features("real002", example, seed, split="test") for example in test]
    train_family = [example.family for example in train]
    train_parameter = [example.parameter for example in train]
    train_joint = [joint_id(example.family, example.parameter) for example in train]
    family_pred = nearest_centroid_predict(train_features, train_family, test_features, N_FAMILIES)
    parameter_pred = nearest_centroid_predict(train_features, train_parameter, test_features, N_PARAMETERS)
    joint_pred = nearest_centroid_predict(train_features, train_joint, test_features, N_FAMILIES * N_PARAMETERS)
    decoded = [executable_answer(family_from_joint(joint), parameter_from_joint(joint), example.query) for joint, example in zip(joint_pred, test)]
    return {
        "family_accuracy": accuracy(family_pred, [example.family for example in test]),
        "parameter_accuracy": accuracy(parameter_pred, [example.parameter for example in test]),
        "joint_accuracy": accuracy(joint_pred, [joint_id(example.family, example.parameter) for example in test]),
        "decoded_answer_accuracy": accuracy(decoded, [example.answer for example in test]),
        "oracle_gap": 1.0 - accuracy(decoded, [example.answer for example in test]),
    }


def evaluate_explicit_slot_sufficiency(test: list[BalancedExample]) -> dict[str, float]:
    answers = []
    for example in test:
        slot = make_slot(example.family, example.parameter)
        compiled = compile_slot(slot)
        answers.append(executable_answer(compiled.family_id, compiled.parameter_id, example.query))
    return {
        "executor_accuracy": accuracy(answers, [example.answer for example in test]),
        "mechanism_is_symbolic": 1.0,
        "learned_from_latent": 0.0,
    }


def evaluate_composition(split: dict[str, Any]) -> dict[str, float]:
    seen_answers = [execute_slot(make_slot(example.family, example.parameter), example) for example in split["seen_eval"]]
    heldout_answers = [execute_slot(make_slot(example.family, example.parameter), example) for example in split["heldout_eval"]]
    pair_lookup = score_pair_lookup(split["train"], split["heldout_eval"])
    train_pairs = {(example.family, example.parameter) for example in split["train"]}
    heldout_pairs = set(split["heldout_pairs"])
    return {
        "seen_accuracy": accuracy(seen_answers, [example.answer for example in split["seen_eval"]]),
        "heldout_accuracy": accuracy(heldout_answers, [example.answer for example in split["heldout_eval"]]),
        "pair_lookup_heldout_accuracy": pair_lookup["heldout_pair_executor_accuracy"] if "heldout_pair_executor_accuracy" in pair_lookup else pair_lookup["executor_accuracy"],
        "heldout_leakage_detected": float(bool(train_pairs & heldout_pairs)),
        "symbolic_not_learned": 1.0,
    }


def evaluate_non_oracle_repair(examples: list[BalancedExample], seed: int) -> dict[str, float]:
    cases = [make_corrupted_case(example, index + seed) for index, example in enumerate(examples)]
    unrepaired_answers = [execute_slot(case["corrupted_slot"], case["example"]) for case in cases]
    repaired_slots = [non_oracle_repair(case["corrupted_slot"]) for case in cases]
    repaired_answers = [execute_slot(slot, case["example"]) for slot, case in zip(repaired_slots, cases)]
    oracle_slots = [make_slot(case["example"].family, case["example"].parameter) for case in cases]
    oracle_answers = [execute_slot(slot, case["example"]) for slot, case in zip(oracle_slots, cases)]
    gold_answers = [case["example"].answer for case in cases]
    detect_pred = [non_oracle_detect(case["corrupted_slot"]) for case in cases]
    detect_gold = [not same_slot(case["corrupted_slot"], make_slot(case["example"].family, case["example"].parameter)) for case in cases]
    repair_hits = [same_slot(slot, make_slot(case["example"].family, case["example"].parameter)) for slot, case in zip(repaired_slots, cases)]
    return {
        "unrepaired_accuracy": accuracy(unrepaired_answers, gold_answers),
        "repaired_accuracy": accuracy(repaired_answers, gold_answers),
        "oracle_repair_accuracy": accuracy(oracle_answers, gold_answers),
        "repair_gain": accuracy(repaired_answers, gold_answers) - accuracy(unrepaired_answers, gold_answers),
        "repair_gap_to_oracle": accuracy(oracle_answers, gold_answers) - accuracy(repaired_answers, gold_answers),
        "detect_accuracy": accuracy_bool(detect_pred, detect_gold),
        "repair_slot_accuracy": mean([float(value) for value in repair_hits]),
        "uses_gold_slot": 0.0,
        "uses_corruption_label": 0.0,
    }


def make_corrupted_case(example: BalancedExample, index: int) -> dict[str, Any]:
    corruption_id = index % 6
    family = example.family
    parameter = example.parameter
    binding = joint_id(family, parameter)
    if corruption_id == 0:
        family = (family + 1) % N_FAMILIES
        binding = joint_id(family, parameter)
    elif corruption_id == 1:
        parameter = (parameter + 1) % N_PARAMETERS
        binding = joint_id(family, parameter)
    elif corruption_id == 2:
        binding = (binding + 1) % (N_FAMILIES * N_PARAMETERS)
    elif corruption_id == 3:
        family = (family + 1) % N_FAMILIES
        parameter = (parameter + 1) % N_PARAMETERS
        binding = joint_id(family, parameter)
    elif corruption_id == 4:
        parameter = (parameter + 1) % N_PARAMETERS
        binding = (joint_id(family, parameter) + 1) % (N_FAMILIES * N_PARAMETERS)
    return {
        "example": example,
        "corrupted_slot": StructureSlot(family, parameter, binding, tuple(one_hot(binding, N_FAMILIES * N_PARAMETERS)), route_id=family),
    }


def non_oracle_detect(slot: StructureSlot) -> bool:
    return slot.binding_id != joint_id(slot.family_id, slot.parameter_id)


def non_oracle_repair(slot: StructureSlot) -> StructureSlot:
    if not non_oracle_detect(slot):
        return slot
    repaired_binding = joint_id(slot.family_id, slot.parameter_id)
    return StructureSlot(
        slot.family_id,
        slot.parameter_id,
        repaired_binding,
        tuple(one_hot(repaired_binding, N_FAMILIES * N_PARAMETERS)),
        route_id=slot.family_id,
    )


def evaluate_claims(metrics: dict[str, float]) -> dict[str, dict[str, Any]]:
    claims: dict[str, dict[str, Any]] = {}
    claims["benchmark_requires_family_and_parameter"] = {
        "passed": bool(metrics["benchmark_valid"] >= 1.0),
        "evidence": {
            "oracle_accuracy": metrics["benchmark_oracle_accuracy"],
            "family_only_accuracy": metrics["benchmark_family_only_accuracy"],
            "parameter_only_accuracy": metrics["benchmark_parameter_only_accuracy"],
        },
    }
    claims["latent_proxy_recovers_executable_structure"] = {
        "passed": bool(
            metrics["latent_family_accuracy"] >= 0.90
            and metrics["latent_parameter_accuracy"] >= 0.70
            and metrics["latent_joint_accuracy"] >= 0.50
            and metrics["latent_decoded_answer_accuracy"] >= 0.50
        ),
        "evidence": {
            "family_accuracy": metrics["latent_family_accuracy"],
            "parameter_accuracy": metrics["latent_parameter_accuracy"],
            "joint_accuracy": metrics["latent_joint_accuracy"],
            "decoded_answer_accuracy": metrics["latent_decoded_answer_accuracy"],
        },
    }
    claims["explicit_bound_slots_are_sufficient"] = {
        "passed": bool(metrics["slots_executor_accuracy"] >= 0.95),
        "evidence": {"executor_accuracy": metrics["slots_executor_accuracy"], "learned_from_latent": metrics["slots_learned_from_latent"]},
    }
    claims["explicit_bound_slots_generalize_compositionally"] = {
        "passed": bool(
            metrics["composition_heldout_accuracy"] >= 0.90
            and metrics["composition_pair_lookup_heldout_accuracy"] <= 0.30
            and metrics["composition_heldout_leakage_detected"] == 0.0
        ),
        "evidence": {
            "heldout_accuracy": metrics["composition_heldout_accuracy"],
            "pair_lookup_heldout_accuracy": metrics["composition_pair_lookup_heldout_accuracy"],
            "symbolic_not_learned": metrics["composition_symbolic_not_learned"],
        },
    }
    claims["non_oracle_verifier_repairs_corrupted_slots"] = {
        "passed": bool(
            metrics["repair_repaired_accuracy"] >= 0.75
            and metrics["repair_repair_gain"] > 0.20
            and metrics["repair_repair_gap_to_oracle"] < 0.20
            and metrics["repair_uses_gold_slot"] == 0.0
            and metrics["repair_uses_corruption_label"] == 0.0
        ),
        "evidence": {
            "unrepaired_accuracy": metrics["repair_unrepaired_accuracy"],
            "repaired_accuracy": metrics["repair_repaired_accuracy"],
            "repair_gain": metrics["repair_repair_gain"],
            "repair_gap_to_oracle": metrics["repair_repair_gap_to_oracle"],
            "detect_accuracy": metrics["repair_detect_accuracy"],
        },
    }
    return claims


def compute_verdict(claims: dict[str, dict[str, Any]]) -> str:
    if claims["latent_proxy_recovers_executable_structure"]["passed"] and claims["non_oracle_verifier_repairs_corrupted_slots"]["passed"]:
        return "VALIDATED: LATENT EXECUTABLE STRUCTURE AND NON-ORACLE REPAIR"
    if claims["explicit_bound_slots_are_sufficient"]["passed"] and claims["explicit_bound_slots_generalize_compositionally"]["passed"]:
        return "PARTIAL: EXPLICIT SLOT SYMBOLIC SUBSTRATE ONLY"
    return "REAL CLAIMS NOT VALIDATED"


def interpret(claims: dict[str, dict[str, Any]], verdict: str) -> str:
    if verdict == "VALIDATED: LATENT EXECUTABLE STRUCTURE AND NON-ORACLE REPAIR":
        return "The corrected suite supports latent executable recovery and non-oracle repair under the current benchmark."
    if verdict == "PARTIAL: EXPLICIT SLOT SYMBOLIC SUBSTRATE ONLY":
        return "The corrected suite supports explicit-slot symbolic execution, but not latent structure discovery or non-oracle structure repair."
    return "The corrected suite does not validate the desired research claims."


def make_slot(family: int, parameter: int) -> StructureSlot:
    binding = joint_id(family, parameter)
    return StructureSlot(family, parameter, binding, tuple(one_hot(binding, N_FAMILIES * N_PARAMETERS)), route_id=family)


def execute_slot(slot: StructureSlot, example: BalancedExample) -> int:
    compiled = compile_slot(slot)
    return executable_answer(compiled.family_id, compiled.parameter_id, example.query)


def same_slot(left: StructureSlot, right: StructureSlot) -> bool:
    return left.family_id == right.family_id and left.parameter_id == right.parameter_id and left.binding_id == right.binding_id


def family_from_joint(value: int) -> int:
    return value // N_PARAMETERS


def parameter_from_joint(value: int) -> int:
    return value % N_PARAMETERS


def prefix(name: str, metrics: dict[str, float]) -> dict[str, float]:
    return {f"{name}_{key}": value for key, value in metrics.items()}


def accuracy(predictions: list[int], labels: list[int]) -> float:
    return mean([float(pred == label) for pred, label in zip(predictions, labels)])


def accuracy_bool(predictions: list[bool], labels: list[bool]) -> float:
    return mean([float(pred == label) for pred, label in zip(predictions, labels)])


def aggregate_rows(rows: list[dict[str, Any]]) -> dict[str, float]:
    metrics: dict[str, float] = {}
    for key, value in rows[0].items():
        if isinstance(value, (int, float)):
            values = [float(row[key]) for row in rows]
            metrics[key] = mean(values)
            metrics[f"{key}_std"] = pstdev(values)
    return metrics


def write_outputs(output_path: Path, result: dict[str, Any]) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(result, indent=2, sort_keys=True), encoding="utf-8")
    rows = [{"claim": claim, "passed": data["passed"], **data["evidence"]} for claim, data in result["claim_results"].items()]
    write_csv(output_path.parent / "claim_results.csv", rows)


def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    fields = sorted({key for row in rows for key in row})
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--seeds", nargs="+", type=int, default=list(range(10)))
    parser.add_argument("--train-samples", type=int, default=256)
    parser.add_argument("--eval-samples", type=int, default=256)
    parser.add_argument("--steps", type=int, default=10)
    parser.add_argument("--output-json", type=Path, default=Path("outputs/real018_claim_audit/metrics.json"))
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    result = run_tac_scm_real018_claim_audit(
        seeds=args.seeds,
        train_samples=args.train_samples,
        eval_samples=args.eval_samples,
        steps=args.steps,
    )
    write_outputs(args.output_json, result)
    print(json.dumps({"verdict": result["verdict"], "claim_results": result["claim_results"]}, indent=2))


if __name__ == "__main__":
    main()
